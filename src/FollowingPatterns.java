	boolean areFollowingPatterns(String[] strings, String[] patterns) {
		boolean isFollowingPattern = true;
		Map<String, Long> stringMap = new LinkedHashMap<String, Long>();
		Map<String, Long> patternMap = new LinkedHashMap<String, Long>();
		
		long idx=0;
		for (String s : strings) {
			if (!stringMap.containsKey(s)) {
				stringMap.put(s, idx++);
			}
		}
		
		idx=0;
		for (String s : patterns) {
			if (!patternMap.containsKey(s)) {
				patternMap.put(s, idx++);
			}
		}

		//compare maps size for easy check
		if (stringMap.size() != patternMap.size()) {
			isFollowingPattern = false;
		}
		
		//make sure order is correct
		if (isFollowingPattern) {
			List<Long> patternList = new ArrayList<Long>();

			List<Long> stringList = new ArrayList<Long>();
			for (int i=0;i<strings.length;i++) {
				stringList.add(stringMap.get(strings[i]));
			}
			for (int i=0;i<patterns.length;i++) {
				patternList.add(patternMap.get(patterns[i]));
			}
			
			for (int i=0;i<stringList.size();i++) {
				if (stringList.get(i) - patternList.get(i) != 0) {
//					System.out.println("setting false because at i: " + i + " these values: " + stringList.get(i) + " vs " + patternList.get(i));
//					System.out.println(stringList.get(i) - patternList.get(i));
					
					isFollowingPattern = false;
					break;
				}
			}
			
		}

		return isFollowingPattern;
	}

